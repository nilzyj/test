# 输出
# 把希望打印的文字用单引号或者双引号括起来，但不能混用单引号和双引号
print('hello world')
# print()函数也可以接受多个字符串，用逗号“,”隔开，就可以连成一串输出,print()会依次打印每个字符串，
# 遇到逗号“,”会输出一个空格
print('The quick brown fox', 'jumps over', 'the lazy dog')
print(20)
print(20 + 30)
print('20 + 30 = ', 20 + 30)

# 输入
name = input()
print(name)
age = input("please input your age")
print(age)

print('1024 * 768 = ', 1024 * 768)