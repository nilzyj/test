package com.mingrisoft;

public class GetDay { // 创建类
	public static void main(String[] args) {							 	// 主方法
		int arr[] = new int[] { 2, 5, 8, 10  }; 	// 创建并初始化一维数组
		for (int i = 0; i < arr.length; i++) {									    	// 利用循环将信息输出
			System.out.print( arr[i] +  " "); 			// 输出信息																		
		}
	}
}

